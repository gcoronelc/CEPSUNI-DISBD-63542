
CREATE TABLE CAMPEONATO
( 
	IDCAMPEONATO         integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(200)  NOT NULL ,
	ANIO                 integer  NOT NULL ,
	SPONSOR              varchar(150)  NOT NULL ,
	CANTEQUIPOS          integer  NOT NULL ,
	CONSTRAINT XPKCAMPEONATO PRIMARY KEY (IDCAMPEONATO ASC)
)
go



CREATE TABLE EQUIPO
( 
	IDEQUIPO             integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(200)  NOT NULL ,
	REPRESENTANTE        varchar(150)  NOT NULL ,
	DIRECCION            varchar(150)  NOT NULL ,
	CONSTRAINT XPKEQUIPO PRIMARY KEY (IDEQUIPO ASC)
)
go



CREATE TABLE INSCRIPCION
( 
	IDINSCRIPCION        integer IDENTITY ( 1,1 ) ,
	IDCAMPEONATO         integer  NOT NULL ,
	IDEQUIPO             integer  NOT NULL ,
	CONSTRAINT XPKINSCRIPCION PRIMARY KEY (IDINSCRIPCION ASC),
	CONSTRAINT FK_INSCRIPCION_CAMPEONATO FOREIGN KEY (IDCAMPEONATO) REFERENCES CAMPEONATO(IDCAMPEONATO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_INSCRIPCION_EQUIPO FOREIGN KEY (IDEQUIPO) REFERENCES EQUIPO(IDEQUIPO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE PAIS
( 
	IDPAIS               integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(200)  NOT NULL ,
	CONSTRAINT XPKPAIS PRIMARY KEY (IDPAIS ASC)
)
go



CREATE TABLE JUGADOR
( 
	IDJUGADOR            integer IDENTITY ( 1,1 ) ,
	IDINSCRIPCION        integer  NOT NULL ,
	NOMBRE               varchar(200)  NOT NULL ,
	DIRECCION            varchar(150)  NOT NULL ,
	IDPAIS               integer  NOT NULL ,
	CONSTRAINT XPKJUGADOR PRIMARY KEY (IDJUGADOR ASC),
	CONSTRAINT FK_JUGADOR_INSCRIPCION FOREIGN KEY (IDINSCRIPCION) REFERENCES INSCRIPCION(IDINSCRIPCION)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_JUGADOR_PAIS FOREIGN KEY (IDPAIS) REFERENCES PAIS(IDPAIS)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE ENTRENADOR
( 
	IDENTRENADOR         integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(200)  NOT NULL ,
	IDINSCRIPCION        integer  NOT NULL ,
	ACTIVO               integer  NOT NULL ,
	CONSTRAINT XPKENTRENADOR PRIMARY KEY (IDENTRENADOR ASC),
	CONSTRAINT FK_ENTRENADOR_INSCRIPCION FOREIGN KEY (IDINSCRIPCION) REFERENCES INSCRIPCION(IDINSCRIPCION)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go


